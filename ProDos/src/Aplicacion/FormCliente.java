    private void tablaClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaClienteMouseClicked
        int fila = tablaCliente.getSelectedRow();
        
        int idprovin  = Integer.parseInt(tablaCliente.getValueAt(fila,2).toString());
        int idpais    = Integer.parseInt(tablaCliente.getValueAt(fila, 3).toString());
        int idciudad  = Integer.parseInt(tablaCliente.getValueAt(fila,1).toString());
        
        String nomciu = tablaCliente.getValueAt(fila,7).toString();
        String provin = tablaCliente.getValueAt(fila,8).toString();
        String nompas = tablaCliente.getValueAt(fila,9).toString();
        String edad   = tablaCliente.getValueAt(fila,5).toString();
        String nombre = tablaCliente.getValueAt(fila,4).toString();
        String numdoc = tablaCliente.getValueAt(fila, 6).toString();
        
        Pais pais = new Pais(idpais,nompas);
        Provincia prov = new Provincia(idprovin,provin);
        Ciudad ciu = new Ciudad(idciudad,nomciu);
        
        nombrecli.setText(nombre);
        numdoccli.setText(numdoc);
        edadcli.setText(edad);
        comboPais.getModel().setSelectedItem(pais);
        comboProvincia.getModel().setSelectedItem(prov);
        comboCiudad.getModel().setSelectedItem(ciu);
    }//GEN-LAST:event_tablaClienteMouseClicked

    private void modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarActionPerformed
        ControlForm control = new ControlForm();
        Cliente cliente     = new Cliente();
        Ciudad ciu      = (Ciudad) comboCiudad.getSelectedItem();
        
        int fila = tablaCliente.getSelectedRow();
        int id   = Integer.parseInt(tablaCliente.getValueAt(fila,0).toString());
        int edad = Integer.parseInt(edadcli.getText());
        
        String nombre = nombrecli.getText();
        String numdoc = numdoccli.getText();
        int idciu  = ciu.getId();
        
        cliente.modificar(id, nombre, edad, numdoc,idciu);
        tablaCliente.setModel(control.cargarTabla("",cliente));
        control.ocultarColumnas(4, tablaCliente);
        
        System.out.println(id+nombre+edad+numdoc+idciu);
        nombrecli.setText("");
        edadcli.setText("");
        numdoccli.setText("");
        comboPais.setSelectedIndex(0);
        comboProvincia.setSelectedIndex(0);
        
    }//GEN-LAST:event_modificarActionPerformed

