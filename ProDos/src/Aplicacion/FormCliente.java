    private void insertarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertarClienteActionPerformed
        Cliente cli = new Cliente();
        ControlForm control = new ControlForm();
        
        String nombre  = nombrecli.getText();
        int edad       = Integer.parseInt(edadcli.getText());
        String numdoc  = numdoccli.getText();
        Provincia prov = (Provincia) comboProvincia.getSelectedItem();
        
        cli.insertar(nombre, edad, numdoc, prov.getId());
        tablaCliente.setModel(control.cargarTabla("",cli));
        control.ocultarColumnas(4, tablaCliente);
    }//GEN-LAST:event_insertarClienteActionPerformed

